		;		the MOV instruction is very simple, it takes in the arguments of a register (ie R0) and a value (ie #01)
		MOV		R0, #01 ; copies a value of one into register 0
		
		;		you can also copy registers into other registers like so,
		
		MOV		R3, R0 ; the first parameter is the destination and the second is the value you want copied 
